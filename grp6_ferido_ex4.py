# Integer values
int1 = 15
int2 = 5
print("INTEGER OPERATIONS")
print("Addition:", int1 + int2)
print("Subtraction:", int1 - int2)
print("Multiplication:", int1 * int2)
print("Division:", int1 / int2)
print("Modulus:", int1 % int2)
print("Exponent:", int1 ** int2)

# Float values
float1 = 20.5
float2 = 3.5
print("FLOAT OPERATIONS")
print("Addition:", float1 + float2)
print("Subtraction:", float1 - float2)
print("Multiplication:", float1 * float2)
print("Division:", float1 / float2)
print("Modulus:", float1 % float2)
print("Exponent:", float1 ** float2)

# Complex values
comp1 = 4 + 3j
comp2 = 3 + 2j
print("COMPLEX OPERATIONS")
print("Addition:", comp1 + comp2)
print("Subtraction:", comp1 - comp2)
print("Multiplication:", comp1 * comp2)
print("Division:", comp1 / comp2)
print("Exponent:", comp1 ** 2)

